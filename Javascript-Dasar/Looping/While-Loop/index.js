// Sebelum mulai loop, tentukan nilai awal
let nilaiAwal = 0; // Nilai Awal 

// Kondisi Terminasi
// Cek kondisi, Apakah bernilai true atau false 
while (nilaiAwal < 10) {

    // Aksi
    // Cetak ini jika kondisi bernilai true
    console.info("Nilai ke " + nilaiAwal);

    // Increment
    // Nilai awal akan bertambah atau looping tergantung dari kondisi terminasi
    // Jika increment telah terpenuhi dan nilai awal menjadi false maka loop berhenti
    nilaiAwal++
}